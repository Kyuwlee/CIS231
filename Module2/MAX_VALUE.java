package Module2;

public class MAX_VALUE {
	public static void main(String[] args) {
		int num;

		num = Integer.MAX_VALUE;
		System.out.printf(" Max Value  %d%n", num);

		num += 1;
		System.out.printf(" Max Value + 1  %d%n", num);

		float fvalue;
		fvalue = Float.MAX_VALUE;
		System.out.printf(" Max Value in Float %f%n", fvalue);

		num += 1;
		System.out.printf(" Max Value in Float + 1  %d%n", num);

	}

}
